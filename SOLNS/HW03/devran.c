int main(
  int    argc,
  char **argv)
{
FILE          *fp;
int            i;
int            rejected = 0;
unsigned char  uc;

Initdbg((&argc,argv));
rdcolor();

fp= fopen("/dev/random","r");
for(i= 0; i < 10; ++i) {
	do {
		uc= fgetc(fp);
		++rejected;
		} while(uc < 31 || uc >= 127);
	--rejected;
	printf("%d: %c\n",i,uc);
	}
printf("rejected %d characters\n",rejected);

Rdbg(("main 0"));
return 0;
}

